class Destination {
  final String id;
  final String name;
  final String route;
  final String imagePath;
  final String price;
  final String date;
  final String duration;
  final double rating;
  final String airline;

  Destination({
    required this.id,
    required this.name,
    required this.route,
    required this.imagePath,
    required this.price,
    required this.date,
    required this.duration,
    required this.rating,
    required this.airline,
  });
}
